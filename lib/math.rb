def addition(num1, num2)
  sum = num1 + num2
  return sum
end

def subtraction(num1, num2)
  difference = num1 - num2
  return difference 
end

def division(num1, num2)
  divide = num1 / num2
  return divide
end

def multiplication(num1, num2)
  product = num1 * num2
  return product
end

def modulo(num1, num2)
  mod_sun = num1 % num2
  return mod_sun
end

def square_root(num)
  sq_rt = Math.sqrt(num)
  return sq_rt
end