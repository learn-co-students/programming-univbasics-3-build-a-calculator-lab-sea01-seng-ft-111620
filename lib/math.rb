def addition(num1, num2)
  sum = num1 + num2
  sum
end

def subtraction(num1, num2)
  minus = num1 - num2
  minus
end

def division(num1, num2)
  divide = num1 / num2
  divide
end

def multiplication(num1, num2)
  multiply = num1 * num2
  multiply
end

def modulo(num1, num2)
  mod = num1 % num2
  mod
end

def square_root(num)
  Math.sqrt(num) 
end